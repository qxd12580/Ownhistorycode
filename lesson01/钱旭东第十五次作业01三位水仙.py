def san():
    for i in range(100,1000):  #从100到999
        x1=i//100   #取百位数字
        x2=i//10%10 #取十位数字
        x3=i%10     #取个位数字
        if x1**3 + x2**3 + x3**3 == i:
            print(f'{i}是水仙花数')
            

#-----------------------第四主程序-------------------------------
print('='*50)
print('100到1000之间所有的水仙花数为')
